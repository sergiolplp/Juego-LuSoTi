var fotopordefecto41 = 'cero1';

function cambiafoto41()
{   
    var imagen41 = document.getElementById('cero');
    if (fotopordefecto41 == 'cero1'){
        imagen41.src = './img/numeros/cero2.jpg';
        fotopordefecto41 = 'cero2';
    }
    else {
        imagen41.src = './img/numeros/cero1.jpg';
        fotopordefecto41 = 'cero1';
    }
}

const audio41 = new Audio();
audio41.src = './mp3/numeros/cero.mp3';


