// A

var fotopordefecto14 = 'a1';

function cambiafoto14()
{   
    var imagen14 = document.getElementById('a');
    if (fotopordefecto14 == 'a1'){
        imagen14.src = './img/letras/a2.jpg.';
        fotopordefecto14 = 'a2';
    }
    else {
        imagen14.src = './img/letras/a1.jpg';
        fotopordefecto14 = 'a1';
    }
}

const audio14 = new Audio();
audio14.src = './mp3/letras/a.mp3';


